def _parse_bool_list(value: str) -> list[bool]:
    """Parse a comma-separated list of booleans: 'true,false' → [True, False]."""
    parts = [v.strip().lower() for v in value.split(",")]
    result = []
    for p in parts:
        if p in ("true", "1", "yes"):
            result.append(True)
        elif p in ("false", "0", "no"):
            result.append(False)
        else:
            raise argparse.ArgumentTypeError(f"Invalid boolean value: {p!r}")
    return result


def _parse_string_list(value: str) -> list[str]:
    """Parse a comma-separated list of strings."""
    return [v.strip() for v in value.split(",")]


def _to_safe_filename(s: str) -> str:
    """Replace characters that are illegal in file/directory names with '_'."""
    return re.sub(r'[\\/:*?"<>|]', "_", s)


def _format_bool_array(arr: list[bool]) -> str:
    """Encode a bool list compactly: [True, False] → 'tf'."""
    return "".join("t" if b else "f" for b in arr)


def _format_string_array(arr: list[str]) -> str:
    """Encode a string list as a safe, joined label."""
    return "+".join(_to_safe_filename(s) for s in arr)


def _post_iteration_action(executable, iteration: int, measurement_dir: Path) -> None:
    """Handle trace/symbol files generated by the k-perf runtime after each iteration."""
    trace_files = list(Path(".").glob("trace*.txt"))
    symbol_files = list(Path(".").glob("symbol*.txt"))
    deleted_files: list[str] = []

    for trace_file in trace_files:
        print(f"- Processing trace file: {trace_file.name}")

        try:
            graph_visualizer_path = (
                _REPO_ROOT / "analyzers" / "call_graph_visualizer" / "graph-visualizer.py"
            )
            result = subprocess.run(
                [sys.executable, str(graph_visualizer_path), str(trace_file)],
                capture_output=True,
            )

            if result.returncode == 0:
                png_files = sorted(Path(".").glob("*.png"), key=lambda p: p.stat().st_ctime, reverse=True)
                if png_files:
                    newest_png = png_files[0]
                    new_png_name = f"{executable.name}_{iteration}.png"
                    new_png_path = Path(".") / new_png_name
                    newest_png.rename(new_png_path)
                    import shutil
                    shutil.copy2(str(new_png_path), str(measurement_dir))
                    new_png_path.unlink(missing_ok=True)
                    print(f"-- Copied {new_png_name} to measurements folder")
            else:
                print("-- Skipped call graph PNG (graph-visualizer.py returned non-zero)")
        except Exception as e:
            print(f"-- Skipped call graph PNG (error: {e})")

        trace_file.unlink(missing_ok=True)
        deleted_files.append(trace_file.name)

    for symbol_file in symbol_files:
        symbol_file.unlink(missing_ok=True)
        deleted_files.append(symbol_file.name)

    if deleted_files:
        print(f"-- Deleted: {', '.join(deleted_files)}")


def main() -> None:
    parser = argparse.ArgumentParser(
        description="K-Perf benchmark suite for Game of Life KMP.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )

    parser.add_argument(
        "--repetition-count", type=int, default=3, metavar="N",
        help="Number of timed iterations per executable (default: 3).",
    )
    parser.add_argument(
        "--clean-build", type=lambda v: v.lower() in ("true", "1", "yes"),
        default=False, metavar="BOOL",
        help="Rebuild all dependencies before benchmarking (default: false).",
    )
    parser.add_argument(
        "--step-count", type=int, default=20, metavar="N",
        help="Number of Game of Life simulation steps per run (default: 20).",
    )
    parser.add_argument(
        "--reference", type=lambda v: v.lower() in ("true", "1", "yes"),
        default=True, metavar="BOOL",
        help="Include uninstrumented reference executables (default: true).",
    )
    parser.add_argument(
        "--common", type=lambda v: v.lower() in ("true", "1", "yes"),
        default=True, metavar="BOOL",
        help="Include CommonMain game type (default: true).",
    )
    parser.add_argument(
        "--dedicated", type=lambda v: v.lower() in ("true", "1", "yes"),
        default=False, metavar="BOOL",
        help="Include DedicatedMain game type (default: false).",
    )
    parser.add_argument(
        "--jvm", type=lambda v: v.lower() in ("true", "1", "yes"),
        default=True, metavar="BOOL",
        help="Include JVM (JAR) targets (default: true).",
    )
    parser.add_argument(
        "--js", type=lambda v: v.lower() in ("true", "1", "yes"),
        default=True, metavar="BOOL",
        help="Include JS (Node.js) targets (default: true).",
    )
    parser.add_argument(
        "--native", type=lambda v: v.lower() in ("true", "1", "yes"),
        default=True, metavar="BOOL",
        help="Include native executable targets (default: true).",
    )
    parser.add_argument(
        "--enabled", type=_parse_bool_list, default=[True], metavar="BOOL[,BOOL...]",
        help="Comma-separated enabled flag values for k-perf configs (default: true).",
    )
    parser.add_argument(
        "--flush-early", type=_parse_bool_list, default=[False], metavar="BOOL[,BOOL...]",
        help="Comma-separated flushEarly flag values (default: false).",
    )
    parser.add_argument(
        "--instrument-property-accessors", type=_parse_bool_list, default=[False],
        metavar="BOOL[,BOOL...]",
        help="Comma-separated instrumentPropertyAccessors flag values (default: false).",
    )
    parser.add_argument(
        "--test-kir", type=_parse_bool_list, default=[False], metavar="BOOL[,BOOL...]",
        help="Comma-separated testKIR flag values (default: false).",
    )
    parser.add_argument(
        "--methods", type=_parse_string_list, default=[".*"], metavar="REGEX[,REGEX...]",
        help="Comma-separated regex patterns for method filtering (default: '.*').",
    )
    parser.add_argument(
        "--ci-label", type=str, default="local", metavar="LABEL",
        help="Label embedded in results for CI identification (default: local).",
    )

    args = parser.parse_args()

    # Validate mutual exclusions
    if not (args.common or args.dedicated):
        print("ERROR: At least one of --common or --dedicated must be true.")
        sys.exit(1)

    if not (args.jvm or args.js or args.native):
        print("ERROR: At least one of --jvm, --js, or --native must be true.")
        sys.exit(1)

    if not args.enabled:
        print("ERROR: --enabled must contain at least one value.")
        sys.exit(1)
    if not args.flush_early:
        print("ERROR: --flush-early must contain at least one value.")
        sys.exit(1)
    if not args.instrument_property_accessors:
        print("ERROR: --instrument-property-accessors must contain at least one value.")
        sys.exit(1)
    if not args.test_kir:
        print("ERROR: --test-kir must contain at least one value.")
        sys.exit(1)
    if not args.methods:
        print("ERROR: --methods must contain at least one regex string value.")
        sys.exit(1)

    # Collect system info
    print("==========================================")
    print("# Collecting System Information...")
    print("==========================================")

    machine_info = get_machine_info(
        _REPO_ROOT / "kmp-examples" / "game-of-life-kmp-commonmain"
    )

    print("System Information collected:")
    for key, val in machine_info.items():
        print(f"  {key} : {val}")
    print()

    # Generate Cartesian product of k-perf configurations
    k_perf_combinations: list[KPerfConfig] = []
    for enabled_val in args.enabled:
        for flush_early_val in args.flush_early:
            for prop_accessors_val in args.instrument_property_accessors:
                for test_kir_val in args.test_kir:
                    for methods_val in args.methods:
                        k_perf_combinations.append(
                            KPerfConfig(
                                enabled=bool(enabled_val),
                                flush_early=bool(flush_early_val),
                                instrument_property_accessors=bool(prop_accessors_val),
                                test_kir=bool(test_kir_val),
                                methods=str(methods_val),
                            )
                        )

    print()
    print("==========================================")
    print("# K-perf Configurations to Build")
    print("==========================================")
    for config in k_perf_combinations:
        print(f"- {get_k_perf_suffix(config)}")

    # --- Clean phase ---
    if args.clean_build:
        print()
        print("==========================================")
        print("# Cleaning k-perf benchmark dependencies...")
        print("==========================================")

        invoke_gradle_clean(_REPO_ROOT / "KIRHelperKit", "KIRHelperKit")
        invoke_gradle_clean(_REPO_ROOT / "plugins" / "k-perf", "k-perf plugin")
        if args.common:
            invoke_gradle_clean(
                _REPO_ROOT / "kmp-examples" / "game-of-life-kmp-commonmain",
                "game-of-life-kmp-commonmain",
            )
            invoke_gradle_clean(
                _REPO_ROOT / "kmp-examples" / "game-of-life-kmp-commonmain-k-perf",
                "game-of-life-kmp-commonmain-k-perf",
            )
        if args.dedicated:
            invoke_gradle_clean(
                _REPO_ROOT / "kmp-examples" / "game-of-life-kmp-dedicatedmain",
                "game-of-life-kmp-dedicatedmain",
            )
            invoke_gradle_clean(
                _REPO_ROOT / "kmp-examples" / "game-of-life-kmp-dedicatedmain-k-perf",
                "game-of-life-kmp-dedicatedmain-k-perf",
            )
        print()
    else:
        print()
        print("==========================================")
        print("# Skipping clean phase (--clean-build false)...")
        print("==========================================")
        print()

    # --- Build phase ---
    build_times: dict[str, float] = {}
    print()
    print("==========================================")
    print("# Building k-perf benchmark dependencies...")
    print("==========================================")

    build_times = merge_dict(build_times, build_kir_helper_kit())
    build_times = merge_dict(build_times, build_k_perf_plugin())

    if args.common:
        build_times = merge_dict(build_times, build_game_of_life_commonmain_reference())
    if args.dedicated:
        build_times = merge_dict(build_times, build_game_of_life_dedicatedmain_reference())

    for config in k_perf_combinations:
        if args.common:
            build_times = merge_dict(
                build_times,
                build_game_of_life_k_perf_variant(GameType.CommonMain, config),
            )
        if args.dedicated:
            build_times = merge_dict(
                build_times,
                build_game_of_life_k_perf_variant(GameType.DedicatedMain, config),
            )

    print()
    print("==========================================")
    print("# Build phase completed successfully!")
    print("==========================================")

    # Collect executables
    native_ext = ".exe" if sys.platform.startswith("win") else ".kexe"

    selected_game_types: list[GameType] = []
    if args.common:
        selected_game_types.append(GameType.CommonMain)
    if args.dedicated:
        selected_game_types.append(GameType.DedicatedMain)

    executables = invoke_get_executables(
        game_types=selected_game_types,
        k_perf_combinations=k_perf_combinations,
        reference=args.reference,
        jvm=args.jvm,
        js=args.js,
        native=args.native,
        native_ext=native_ext,
        artifact_version=ARTIFACT_VERSION,
    )

    if not executables:
        print("ERROR: No executables match the provided parameters.")
        sys.exit(1)

    print()
    print(f"Selected {len(executables)} executables for benchmarking:")
    for exec_ in executables:
        print(f"  - {exec_.name}")
    print()

    # Clean up leftover trace and symbol files from previous runs
    print("Cleaning up existing trace and symbol files...")
    cleaned_files: list[str] = []
    for pattern in ("trace*.txt", "symbol*.txt"):
        for f in Path(".").glob(pattern):
            f.unlink(missing_ok=True)
            cleaned_files.append(f.name)

    if cleaned_files:
        print(f"Deleted: {', '.join(cleaned_files)}")
    else:
        print("No existing trace/symbol files found.")
    print()

    # Build measurement directory name
    platform_parts: list[str] = []
    if args.jvm:
        platform_parts.append("jvm")
    if args.js:
        platform_parts.append("js")
    if args.native:
        platform_parts.append("nat")
    platform_label = "-".join(platform_parts)

    measurement_timestamp = datetime.now().strftime("%Y_%m_%d_%H_%M_%S")
    ref_flag = "t" if args.reference else "f"
    cmn_flag = "t" if args.common else "f"
    ded_flag = "t" if args.dedicated else "f"

    measurement_dir_name = (
        f"{measurement_timestamp}_k-perf_{args.ci_label}_{args.repetition_count}reps_{args.step_count}steps"
        f"_ref{ref_flag}_cmn{cmn_flag}_ded{ded_flag}_{platform_label}"
        f"_en{_format_bool_array(args.enabled)}"
        f"_fe{_format_bool_array(args.flush_early)}"
        f"_pa{_format_bool_array(args.instrument_property_accessors)}"
        f"_tkir{_format_bool_array(args.test_kir)}"
        f"_m-{_format_string_array(args.methods)}"
    )
    measurement_dir = _REPO_ROOT / "measurements" / measurement_dir_name

    if measurement_dir.exists():
        print(f"ERROR: Measurement directory already exists: {measurement_dir}")
        print("Please try again in a moment to get a different timestamp.")
        sys.exit(1)

    measurement_dir.mkdir(parents=True, exist_ok=False)

    # Suite-level parameters embedded in every result JSON
    suite_parameters = {
        "CILabel": args.ci_label,
        "RepetitionCount": args.repetition_count,
        "CleanBuild": args.clean_build,
        "StepCount": args.step_count,
        "Reference": args.reference,
        "Common": args.common,
        "Dedicated": args.dedicated,
        "JVM": args.jvm,
        "JS": args.js,
        "Native": args.native,
        "Enabled": args.enabled,
        "FlushEarly": args.flush_early,
        "InstrumentPropertyAccessors": args.instrument_property_accessors,
        "TestKIR": args.test_kir,
        "Methods": args.methods,
    }

    invoke_benchmark_suite(
        executables=executables,
        repetition_count=args.repetition_count,
        step_count=args.step_count,
        measurement_dir=measurement_dir,
        machine_info=machine_info,
        build_times=build_times,
        parameters=suite_parameters,
        clean_build=args.clean_build,
        post_iteration_action=_post_iteration_action,
    )

    sys.exit(0)


if __name__ == "__main__":
    main()
